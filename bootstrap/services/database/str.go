package database

func StrPointer(str string) *string {
	return &str
}

func IntPointer(i int) *int {
	return &i
}

func Int64Pointer(i int64) *int64 {
	return &i
}

func UInt64Pointer(i uint64) *uint64 {
	return &i
}

func UInt32Pointer(i uint32) *uint32 {
	return &i
}

func Int32Pointer(i int32) *int32 {
	return &i
}
